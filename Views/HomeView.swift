    // MARK: - Derived Values

    /// A warm, time-of-day greeting for the header.
    private var greeting: String {
        switch Calendar.current.component(.hour, from: Date()) {
        case 5..<12: return "Good morning"
        case 12..<17: return "Good afternoon"
        case 17..<22: return "Good evening"
        default: return "Hello"
        }
    }

    private var todayDateText: String {
        Date().formatted(.dateTime.weekday(.wide).month(.abbreviated).day())
    }

    private var monthlyProgress: Double {
        guard monthlyTargetCount > 0 else { return 0 }
        return min(Double(monthlyConnectedCount) / Double(monthlyTargetCount), 1.0)
    }

    // Warm, forward-looking copy. We deliberately avoid pace/behind framing —
    // the goal is a gentle nudge, never guilt about a number you "should" hit.
    private var monthlyReachedLabel: String {
        switch monthlyConnectedCount {
        case 0:
            return "Reach out to someone today"
        case 1:
            return "1 person reached this month"
        default:
            return "\(monthlyConnectedCount) reached this month"
        }
    }

    private var progressMessage: String {
        if monthlyConnectedCount == 0 {
            return "A fresh start"
        }
        if monthlyTargetCount > 0, monthlyConnectedCount >= monthlyTargetCount {
            return "You’re on a roll"
        }
        return "Keep it going"
    }

    private var progressTintColor: Color {
        if monthlyTargetCount > 0, monthlyConnectedCount >= monthlyTargetCount {
            return Theme.Palette.success
        }
        return Theme.Palette.brand
    }

    // MARK: - Private Helpers

    /// Runs the once-per-day housekeeping and, on the first open of a new day,
    /// generates today's picks automatically. If picks already exist (or the
    /// user reset them today) it just reloads what's there.
    private func loadOrAutoGenerate() {
        let viewModel = TodayViewModel(context: context)
        try? viewModel.monthRolloverIfNeeded()
        poolWarningText = (try? viewModel.poolWarning()) ?? nil

        let existing = (try? viewModel.loadTodayPicks()) ?? []
        if existing.isEmpty, lastAutoGeneratedDay != todayKey {
            let generated = (try? viewModel.generateTodayPicks()) ?? []
            if !generated.isEmpty {
                lastAutoGeneratedDay = todayKey
            }
        }

        refreshTodayPicks()
    }

    private var todayKey: String {
        let comps = Calendar.current.dateComponents([.year, .month, .day], from: Date())
        return "\(comps.year ?? 0)-\(comps.month ?? 0)-\(comps.day ?? 0)"
    }

    private func refreshTodayPicks() {
        let viewModel = TodayViewModel(context: context)
        let people = (try? viewModel.loadTodayPicks()) ?? []
        guard !people.isEmpty else {
            withMotion(.easeInOut(duration: 0.25)) {
                todayPicks = []
            }
            selectedPick = nil
            refreshMonthlyProgress()
            refreshStreak()
            return
        }

        // Build the rows from Core Data immediately; phone numbers are
        // resolved from the Contacts store asynchronously so we never block
        // the main thread on every appear/foreground/tab switch.
        let rebuilt: [HomePick] = people.compactMap { person in
            guard let identifier = person.contactIdentifier else { return nil }
            return HomePick(
                identifier: identifier,
                displayName: person.displayName ?? "Unknown",
                phoneNumber: nil,
                lastConnectedText: lastConnectedText(for: person),
                hasConnectedBefore: person.lastCalledAt != nil,
                note: person.note
            )
        }

        withMotion(.spring(response: 0.45, dampingFraction: 0.85)) {
            todayPicks = rebuilt
        }

        selectedPick = todayPicks.first
        refreshMonthlyProgress()
        refreshStreak()

        let identifiers = todayPicks.map(\.identifier)
        Task {
            let numbers = await Self.loadPhoneNumbers(for: identifiers)
            applyPhoneNumbers(numbers)
        }
    }

    /// Runs the given state change with an animation, unless the user has asked
    /// the system to reduce motion — in which case it applies instantly.
    private func withMotion(_ animation: Animation, _ change: () -> Void) {
        if reduceMotion {
            change()
        } else {
            withAnimation(animation, change)
        }
    }

    private func refreshStreak() {
        let request: NSFetchRequest<ConnectionEvent> = ConnectionEvent.fetchRequest()
        request.sortDescriptors = [NSSortDescriptor(key: "date", ascending: false)]
        guard let events = try? context.fetch(request) else {
            currentStreak = 0
            return
        }
        currentStreak = NotificationsService.streaks(from: events).current
    }

    private func applyPhoneNumbers(_ numbers: [String: String]) {
        guard !numbers.isEmpty else { return }

        todayPicks = todayPicks.map { pick in
            guard let number = numbers[pick.identifier] else { return pick }
            return HomePick(
                identifier: pick.identifier,
                displayName: pick.displayName,
                phoneNumber: number,
                lastConnectedText: pick.lastConnectedText,
                hasConnectedBefore: pick.hasConnectedBefore,
                note: pick.note
            )
        }

        if let selectedID = selectedPick?.identifier {
            selectedPick = todayPicks.first { $0.identifier == selectedID }
        } else {
            selectedPick = todayPicks.first
        }
    }

    // MARK: - Actions
    private func generateTodayPick() {
        do {
            let viewModel = TodayViewModel(context: context)
            let picks = try viewModel.generateTodayPicks()
            guard !picks.isEmpty else {
                todayPicks = []
                selectedPick = nil
                return
            }
            refreshTodayPicks()
        } catch {
            connectErrorMessage = "Couldn’t generate today’s pick."
            showConnectError = true
        }
    }

    private func resetTodayPick() {
        do {
            let viewModel = TodayViewModel(context: context)
            try viewModel.resetTodayPicks()
            // Mark today as handled so auto-generate doesn't immediately
            // recreate the set the user just cleared.
            lastAutoGeneratedDay = todayKey
            refreshTodayPicks()
        } catch {
            connectErrorMessage = "Couldn’t reset today’s pick."
            showConnectError = true
        }
    }

    private func markPickAsCalled(_ pick: HomePick) {
        do {
            let viewModel = TodayViewModel(context: context)
            if let person = try viewModel.person(for: pick.identifier) {
                try viewModel.markCalled(person)
            }
            celebrateConnection(for: pick.identifier)
            refreshTodayPicks()
            refreshMonthlyProgress()
            refreshStreak()
        } catch {
            connectErrorMessage = "Couldn’t mark this contact as called."
            showConnectError = true
        }
    }

    /// The little payoff for reaching out — a success haptic plus a brief badge
    /// on the person's row. Deliberately short and quiet, in keeping with the
    /// app's calm tone.
    private func celebrateConnection(for identifier: String) {
        successHaptic()
        withMotion(.spring(response: 0.4, dampingFraction: 0.6)) {
            justConnectedID = identifier
        }
        Task {
            try? await Task.sleep(for: .seconds(1.4))
            withMotion(.easeOut(duration: 0.3)) {
                if justConnectedID == identifier { justConnectedID = nil }
            }
        }
    }

    private func saveNote(for pick: HomePick) {
        do {
            let viewModel = TodayViewModel(context: context)
            try viewModel.setNote(noteDraft, forContactIdentifier: pick.identifier)
            refreshTodayPicks()
            showingPickDetails = false
        } catch {
            connectErrorMessage = "Couldn’t save this note."
            showConnectError = true
        }
    }

    private func snooze(_ pick: HomePick, days: Int) {
        do {
            let viewModel = TodayViewModel(context: context)
            guard let person = try viewModel.person(for: pick.identifier) else { return }

            // Snooze from the start of the day so "just for today" makes them
            // eligible again tomorrow, not at the same clock time.
            let calendar = Calendar.current
            let base = calendar.startOfDay(for: Date())
            let until = calendar.date(byAdding: .day, value: days, to: base) ?? Date()

            try viewModel.snoozePick(person, until: until)
            refreshTodayPicks()
        } catch {
            connectErrorMessage = "Couldn’t snooze this pick."
            showConnectError = true
        }
    }

    private func refreshMonthlyProgress() {
        let calendar = Calendar.current
        let now = Date()

        let startOfMonth = calendar.date(from: calendar.dateComponents([.year, .month], from: now)) ?? now
        let range = calendar.range(of: .day, in: .month, for: now)
        let daysInMonth = range?.count ?? 30

        let picksPerDay: Int
        do {
            picksPerDay = max(try AppSettings.effective(in: context).picksPerDay, 0)
        } catch {
            monthlyConnectedCount = 0
            monthlyTargetCount = 0
            return
        }

        monthlyTargetCount = picksPerDay * daysInMonth

        // Progress denominator: picksPerDay * daysInMonth. The numerator counts
        // ConnectionEvents this month, which markCalled already dedups to at most
        // one per contact per day — so this is "unique contact-days connected"
        // versus the monthly goal, not a raw tap count.
        let request: NSFetchRequest<ConnectionEvent> = ConnectionEvent.fetchRequest()
        request.predicate = NSPredicate(format: "date >= %@", startOfMonth as NSDate)

        guard let monthlyConnections = try? context.count(for: request) else {
            monthlyConnectedCount = 0
            return
        }

        monthlyConnectedCount = monthlyConnections
    }

    /// Resolves phone numbers for the given contact identifiers off the main
    /// thread. Runs on a detached background task so the Contacts store work
    /// never blocks UI updates.
    private static func loadPhoneNumbers(for identifiers: [String]) async -> [String: String] {
        await Task.detached(priority: .utility) {
            let store = CNContactStore()
            let keys: [CNKeyDescriptor] = [CNContactPhoneNumbersKey as CNKeyDescriptor]

            var result: [String: String] = [:]
            for identifier in identifiers where !identifier.isEmpty {
                let predicate = CNContact.predicateForContacts(withIdentifiers: [identifier])
                if let contact = try? store.unifiedContacts(matching: predicate, keysToFetch: keys).first,
                   let number = contact.phoneNumbers.first?.value.stringValue {
                    result[identifier] = number
                }
            }
            return result
        }.value
    }

    private static let relativeDateFormatter: RelativeDateTimeFormatter = {
        let formatter = RelativeDateTimeFormatter()
        formatter.unitsStyle = .full
        return formatter
    }()

    private func lastConnectedText(for person: Person) -> String {
        if let lastCalledAt = person.lastCalledAt {
            return "Last connected: " + Self.relativeDateFormatter.localizedString(for: lastCalledAt, relativeTo: Date())
        }
        return "Not connected yet"
    }

    /// Opens the call/message link and, when it actually launches, records the
    /// check-in automatically — so reaching out is the only step the user takes.
    /// (markCalled dedups to one connection per contact per day.)
    private func connectAndLog(_ scheme: PhoneLink.Scheme, value: String, pick: HomePick) {
        guard connectVia(scheme, value: value) else { return }
        markPickAsCalled(pick)
    }

    @discardableResult
    private func connectVia(_ scheme: PhoneLink.Scheme, value: String) -> Bool {
        guard let url = PhoneLink.url(scheme, number: value) else {
            connectErrorMessage = "No valid phone number for this contact."
            showConnectError = true
            return false
        }

        successHaptic()
        openURL(url)
        return true
    }

    private func lightHaptic() { Haptics.light() }

    private func successHaptic() { Haptics.success() }
}

// MARK: - Home Header

/// The top-of-screen header: a time-aware greeting, the serif app title with
/// today's date, and the streak flame when there's momentum to celebrate.
private struct HomeHeader: View {
    let greeting: String
    let dateText: String
    let streak: Int

    var body: some View {
        HStack(alignment: .top) {
            VStack(alignment: .leading, spacing: 2) {
                Text(greeting)
                    .font(.subheadline.weight(.medium))
                    .foregroundStyle(Theme.Palette.textSecondary)

                Text("StayConnected")
                    .displayTitle()

                Text(dateText)
                    .font(.subheadline)
                    .foregroundStyle(Theme.Palette.textSecondary)
            }

            Spacer(minLength: Theme.Space.sm)

            StreakFlame(streak: streak)
                .padding(.top, 4)
        }
        .frame(maxWidth: .infinity, alignment: .leading)
        .accessibilityElement(children: .combine)
    }
}

// MARK: - Home Empty State

/// A warm, centered empty state — a soft symbol motif above the copy, so an
/// empty day still feels inviting rather than blank.
private struct HomeEmptyState: View {
    /// Custom asset-catalog illustration; falls back to `symbol` when nil.
    var illustration: String? = nil
    let symbol: String
    let title: String
    let message: String

    var body: some View {
        VStack(spacing: Theme.Space.sm) {
            if let illustration {
                Image(illustration)
                    .resizable()
                    .scaledToFit()
                    .frame(width: 84, height: 84)
                    .padding(.bottom, 2)
            } else {
                Image(systemName: symbol)
                    .font(.system(size: 30, weight: .light))
                    .foregroundStyle(Theme.Palette.brand)
                    .padding(.bottom, 2)
            }

            Text(title)
                .font(.subheadline.weight(.semibold))
                .foregroundStyle(Theme.Palette.textPrimary)

            Text(message)
                .font(.footnote)
                .foregroundStyle(Theme.Palette.textSecondary)
                .multilineTextAlignment(.center)
        }
        .frame(maxWidth: .infinity)
        .accessibilityElement(children: .combine)
    }
}

// MARK: - Today Pick Row

/// One person in today's set. Presentation only — all actions are delegated to
/// the closures the Home screen supplies, so this stays free of Core Data.
private struct TodayPickRow: View {
    let pick: HomePick
    let isCelebrating: Bool
    let onConnect: () -> Void
    let onDone: () -> Void
    let onSnooze: () -> Void
    let onOpenDetails: () -> Void

    @Environment(\.accessibilityReduceMotion) private var reduceMotion

    var body: some View {
        HStack(spacing: 10) {
            ContactAvatarInlineView(
                contactIdentifier: pick.identifier,
                displayName: pick.displayName
            )
            .overlay(alignment: .bottomTrailing) {
                if isCelebrating {
                    Image(systemName: "checkmark.circle.fill")
                        .font(.subheadline)
                        .foregroundStyle(Theme.Palette.success)
                        .background(Circle().fill(Theme.Palette.card))
                        .transition(.scale.combined(with: .opacity))
                }
            }

            VStack(alignment: .leading, spacing: 4) {
                // Name gets its own full-width line so it isn't crowded by the
                // status chip; the chip moves down next to the last-connected
                // line, where there's room for both.
                Text(pick.displayName)
                    .font(.body)
                    .fontWeight(.medium)
                    .foregroundStyle(Theme.Palette.textPrimary)
                    .lineLimit(1)

                HStack(spacing: 6) {
                    Chip(
                        text: pick.hasConnectedBefore ? "Connected" : "New",
                        tint: pick.hasConnectedBefore ? Theme.Palette.success : Theme.Palette.brand,
                        fillOpacity: pick.hasConnectedBefore ? 0.16 : 0.14
                    )
                    .fixedSize()
                    .animation(reduceMotion ? nil : .spring(response: 0.4, dampingFraction: 0.7), value: pick.hasConnectedBefore)

                    Text(pick.lastConnectedText)
                        .font(.caption)
                        .foregroundStyle(Theme.Palette.textSecondary)
                        .lineLimit(1)
                }

                if let note = pick.note, !note.isEmpty {
                    Label(note, systemImage: "quote.opening")
                        .font(.caption)
                        .italic()
                        .foregroundStyle(Theme.Palette.brand)
                        .lineLimit(1)
                }
            }
            .frame(maxWidth: .infinity, alignment: .leading)

            HStack(spacing: 6) {
                Button(action: onConnect) {
                    Image(systemName: "phone.fill")
                }
                .buttonStyle(CircularIconButtonStyle(tint: Theme.Palette.success, weight: .filled, diameter: 34))
                .accessibilityLabel("Call or message \(pick.displayName)")
                .accessibilityHint("Opens contact options for this person.")

                Button(action: onDone) {
                    Image(systemName: "checkmark")
                }
                .buttonStyle(CircularIconButtonStyle(tint: Theme.Palette.brand, diameter: 34))
                .accessibilityLabel("Mark \(pick.displayName) as connected")
                .accessibilityHint("Records that you reached out to this person today.")

                Button(action: onSnooze) {
                    Image(systemName: "moon.zzz")
                }
                .buttonStyle(CircularIconButtonStyle(tint: Theme.Palette.textSecondary, diameter: 34))
                .accessibilityLabel("Not today for \(pick.displayName)")
                .accessibilityHint("Gently defers this person and swaps in someone else.")
            }
            .fixedSize()
        }
        .padding(.vertical, 12)
        .padding(.horizontal, 4)
        .contentShape(Rectangle())
        .onTapGesture(perform: onOpenDetails)
    }
