                                ForEach(Array(todayPicks.enumerated()), id: \.element.identifier) { entry in
                                    let index = entry.offset
                                    let pick = entry.element

                                    TodayPickRow(
                                        pick: pick,
                                        isCelebrating: justConnectedID == pick.identifier,
                                        onConnect: {
                                            selectedPick = pick
                                            lightHaptic()
                                            showConnectSheet = true
                                        },
                                        onDone: {
                                            lightHaptic()
                                            markPickAsCalled(pick)
                                        },
                                        onSnooze: {
                                            snoozeTarget = pick
