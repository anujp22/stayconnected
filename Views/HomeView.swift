        if monthlyTargetCount > 0, monthlyConnectedCount >= monthlyTargetCount {
            return "You’re on a roll"
        }
        return "Keep it going"
    }

    private var progressTintColor: Color {
        if monthlyTargetCount > 0, monthlyConnectedCount >= monthlyTargetCount {
            return Color("Success")
