                                    if index < todayPicks.count - 1 {
                                        Divider()
                                            .overlay(Theme.Palette.divider)
                                            .padding(.leading, 58)
                                    }
                                }
                            }
                            .padding(.horizontal, 10)
                            .padding(.vertical, 4)
